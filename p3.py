# Define Width and Height
width = float(input('Please give the width of a rectangle: '))
length = float(input('Please give the length of a rectangle: '))

# Define the Formulas for Area and Perimeter
Area = width * length
Perimeter = 2 * (width + length)

# Display the Computed Answers
print ("\n Area of the Rectangle is: %.2f" %Area)
print (" Perimeter of the Rectangle is: %.2f" %Perimeter)

